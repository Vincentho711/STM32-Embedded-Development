// Callback function for interrupt timer, the callback function gets run when timer resets once it gets to max value
// The int. period is set by changing "Counter Period" in TIM10's "Parameter Settings" in .ioc
// Counter Period = (Desired int. period(s))* (CPU clk speed(MHz)/Prescalar)
// E.g. If we want 1s interrupts and we have 84Mhz CPU clock and prescalar = 8400
// Counter period = 1*(84Mhz/8400) = 10,000
// Remember to enable "TIMX Global Interrupt" in "NVIC Settings" as well
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// Check which version of the timer triggered this callback and read temp
	if (htim == &htim10)
	{
		// Toggle LED2 to signify callback is called
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
		// Save the return value of HAL status
		HAL_StatusTypeDef ret;
		// Read temp from DS1631
		ret = DS1631_read_temp(DS1631_ADDR, DS1631_CONFIG_ADDR, &temperature);
		if (ret != HAL_OK)
		{
			strcpy((char*)buffer,"Error read T\r\n");
			// Sent out error message
			HAL_UART_Transmit(&huart2, buffer, strlen((char*)buffer),50);
		}else
		{
			// Currently float can't be printed on UART so can't display value
			// However, it is stored in the temperature variable
			strcpy((char*)buffer,"Temp read!\r\n");
			HAL_UART_Transmit(&huart2, buffer, strlen((char*)buffer),50);
		}
	}
}
